// GETTING THE DAY OF WEEK FOR WEATHER
const date = new Date();
const day = date.getDay();
let dayOfWeek;

if(day === 0) 
    dayOfWeek = 'Sunday';
else if(day === 1)
    dayOfWeek = 'Monday';
else if(day === 2)
    dayOfWeek = 'Tuesday';
else if(day === 3)
    dayOfWeek = 'Wednesday';
else if(day === 4)
    dayOfWeek = 'Thursday';
else if(day === 5)
    dayOfWeek = 'Friday';
else 
    dayOfWeek = 'Saturday';


